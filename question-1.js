// تابع همزمان (Synchronous):

// مثل یک صف نانواییه. هر مشتری (دستور) به ترتیب میاد و تا کار مشتری قبلی تموم نشه، نوبت مشتری بعدی نمیشه.
// تا زمانی که یک تابع همزمان به طور کامل اجرا نشه، اجرای کد بعدی متوقف میشه.
// به عبارت دیگه، کد خط به خط و به ترتیب اجرا میشه.
// مثال: فرض کنید سه تا کار داریم: پختن کیک، خامه زدن، و تزیین کیک. در حالت همزمان، اول کیک پخته میشه، بعد خامه زده میشه، و در آخر تزیین میشه. تا کیک پخته نشه، خامه زنی شروع نمیشه.

// تابع ناهمزمان (Asynchronous):

// مثل رستورانیه. مشتری‌ها (دستورات) سفارش میدن و میرن میشینن. آشپز (سیستم) به هر سفارشی که آماده شد، به مشتری اطلاع میده. لازم نیست مشتری‌ها منتظر تموم شدن کار بقیه بمونن.
// یک تابع ناهمزمان میتونه کاری رو شروع کنه و در عین حال به اجرای کد بعدی ادامه بده. وقتی کار تابع تموم شد، یک "callback" یا "promise" اجرا میشه و نتیجه رو برمیگردونه.
// به عبارت دیگه، کد منتظر تموم شدن یک کار طولانی نمیمونه و به کارهای دیگه ادامه میده.
// مثال: همون مثال کیک رو در نظر بگیرید. در حالت ناهمزمان، کیک برای پختن گذاشته میشه، و در همین حین، خامه زنی شروع میشه. وقتی کیک پخت، یه علامت (callback) میاد و تزیین کیک شروع میشه. یعنی لازم نیست منتظر تموم شدن کامل پخت کیک بمونیم تا کار بعدی رو شروع کنیم.
